macro stringify_enum(arg: untyped): untyped =
    var stringify = newProc()
    stringify[0] = newNimNode(nnkPostfix)
    stringify[0] &= ident("*")
    stringify[0] &= ident("stringify")
    stringify[3][0] = ident("string")
    var inputs = newNimNode(nnkIdentDefs)
    inputs &= ident("value")
    inputs &= ident("GLenum")
    inputs &= newEmptyNode()
    stringify[3] &= inputs
    stringify[6] &= newNimNode(nnkCaseStmt)
    stringify[6][0] &= newCall(ident("uint32"), ident("value"))
    # var limit = 0
    var previous: seq[int]
    for statement in arg[0]:
        # if limit > 10:
        #     continue
        var ofbranch = newNimNode(nnkOfBranch)
        var enum_val = parseHexInt(statement[2][0].repr)
        if enum_val in previous:
            echo(statement[0].repr)
            continue
        previous &= enum_val
        ofbranch &= newIntLitNode(enum_val)
        var ret = newNimNode(nnkReturnStmt)
        ret &= newStrLitNode(statement[0].repr)
        ofbranch &= ret
        stringify[6][0] &= ofbranch
        # limit += 1
    var elsebranch = newNimNode(nnkElse)
    var ret = newNimNode(nnkReturnStmt)
    ret &= newStrLitNode("unknown enum value")
    elsebranch &= ret
    stringify[6][0] &= elsebranch
    # echo(stringify.treeRepr)
    return stringify


